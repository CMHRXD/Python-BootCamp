#Control Flow in Python

# Control Flow is the order in which the computer executes statements in a script.
# Control Flow is very important in programming as it allows us to execute code based on certain conditions.

# There are 3 types of Control Flow in Python:
# 1. Conditional Statements
# 2. Loops
# 3. Functions


