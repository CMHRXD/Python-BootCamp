# Functions in python

# Functions are defined using the def keyword
# def function_name(parameters):
#   function_body
#   return value_to_return
#
# Functions can be called using the function_name(parameters) syntax
#
# Functions can be called with positional arguments, keyword arguments, or a mix of both
# Example of positional arguments:
#   def printer(data):
#       print(data)
#   printer("Hello World")
# Example of keyword arguments:
#   def printer(data):
#       print(data)
#   printer(data="Hello World")
#


def printer(data):
    print(data)


printer("Hello World")
printer(data="Hello World")
