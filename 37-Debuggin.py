# Debuggin in Python

# Debuggin is the process of finding and fixing errors within a script.

# Syntax errors are usually easy to fix once you find them.
# Examples:
# - Missing colon at the end of a def statement.
# - Missing closing parenthesis at the end of a print statement.
# - Missing closing parenthesis at the end of a function call.


# Logic errors are harder to find and fix.
# Examples:
# - Using a variable before it is defined.
# - Using an undefined variable, object, function, or method.
# - Using the wrong operator, or using an operator in the wrong way.
# - Indentation errors.

# Python has a built-in debugger called pdb.

# To use it, you must import pdb and run pdb.set_trace() where you want to start debugging.

# You can use the following commands to navigate through the debugger:

# l (list)
# n (next line)
# p (print)
# c (continue - finishes debugging)

# You can also use the following commands to navigate through the debugger:

# q (quit)
# h (help)
# w (where)


