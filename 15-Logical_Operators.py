# Logical Operators
#
# Logical Operators are used to perform logical operations on values and variables.
# Logical Operators are also known as Boolean Operators.
#
# There are 3 types of Logical Operators in Python:
#
# 1. AND
# 2. OR
# 3. NOT

# AND Operator
# The AND Operator returns True if both the operands are True.
# The AND Operator returns False if any of the operands is False.
# The AND Operator returns False if both the operands are False.
# The AND Operator is represented by the symbol 'and'.
# The AND Operator is used to check if both the conditions are True.

# OR Operator
# The OR Operator returns True if any of the operands is True.
# The OR Operator returns False if both the operands are False.
# The OR Operator is represented by the symbol 'or'.
# The OR Operator is used to check if any of the conditions is True.

# NOT Operator
# The NOT Operator returns True if the operand is False.
# The NOT Operator returns False if the operand is True.
# The NOT Operator is represented by the symbol 'not'.
# The NOT Operator is used to reverse the result of the condition.

# Example 1:
# AND Operator
print(True and True) # True
print(True and False) # False
print(False and True) # False
print(False and False) # False

# Example 2:
# OR Operator
print(True or True) # True
print(True or False) # True
print(False or True) # True
print(False or False) # False

# Example 3:
# NOT Operator
print(not True) # False
print(not False) # True