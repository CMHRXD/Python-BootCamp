# Conditional Operators in Python

# Conditional Operators are used to perform conditional operations on values and variables.
# Conditional Operators are also known as Relational Operators.

# There are 6 types of Conditional Operators in Python:

# 1. Equal to (==)
# 2. Not Equal to (!=)
# 3. Greater than (>)
# 4. Less than (<)
# 5. Greater than or Equal to (>=)
# 6. Less than or Equal to (<=)


